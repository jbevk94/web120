<!-- About US -->

<p>Piroshky(pi-rawsh-kee)</p>

<p>We opened our doors for the very first time in 1992 hoping to bring a taste of Russia to the Pacific Northwest. Blending traditional recipes with local flavor, our selection of Piroshkies offer a wide array of choices, both savory and sweet.</p>

<p>Traditionally served throughout markets and homes in Eastern Europe, often asked is "What is a Piroshky?" The most simple answer is that they are hand held pies, and their fillings are as diverse and differing as the cultures and people who make and serve them. That is the beauty of Piroshky — everyone makes it a little differently, and recipes are passed down from generation to generation. From babushka to children.</p>

<p>
Here in our bakery at the Pike Place Market, starting two decades ago we decided to embrace and integrate the taste of the Northwest into our own traditional recipes. Our Piroshkies are individually made from scratch and hand-molded into their very own unique shapes — customers are delighted to find that this makes differentiating between them an easier task.</p>

<p>Though our bakery is small, our selection is anything but. We are always happy to offer suggestions and guide you in your selection. C'mon down, breath in the oven's aroma, and take a look at our freshly baked goods. It is not uncommon for Piroshkies to go straight from the oven and into your hands.</p>



