<h3>Black Hitory Month</h3>
<p>
Black History Month is an annual celebration of achievements by African Americans and a time for recognizing the central role of blacks in U.S. history. Other countries around the world, including Canada and the United Kingdom, also devote a month to celebrating black history.
</p>
<p>
The story of Black History Month begins in 1915, half a century after the Thirteenth Amendment abolished slavery in the United States.
</p>
<p>
The Black History Month 2020 theme, “African Americans and the Vote,” is in honor of the centennial anniversary of the Nineteenth Amendment (1920) granting women’s suffrage and the sesquicentennial of the Fifteenth Amendment (1870) giving black men the right to vote.</p>

