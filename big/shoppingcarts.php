<h1>Shopping Carts</h1>
<p>
Adding a shopping cart to your website doesn’t have to be a daunting task. Once you’re ready to start selling your products online you can choose to create an e-commerce site thats main objective is to sell your goods. Or you can add a shopping cart to your already existing site without drastically changing the look and feel of it. (Cannon, 2019)

</p> 
<p>
A shopping cart on a website is a part of e-commerce software on a web server. With this tool visitors coming to your website, after scrolling through your catalog, can add the items they would like to purchase to their virtual shopping cart. Once they decide to check out it will securely accept their payment, the same as if they were in an actual brick and mortar store. 

</p>
<p>
There are many benefits to having a shopping cart on your website. Nowadays most people prefer to shop online for ease and convenience. We’re all busy and shopping online saves time and often helps you find better deals than you would in-store. It adds another platform for customers to purchase your items without the costly expenses of a brick and mortar shop. Also having your purchases delivered right to your door is a nice touch. (Cannon, 2019)
</p>

<p>
There are a few features that make the customer experience more enjoyable that are important to add to your website. It may seem appealing to want to capture information from the people shopping from your website so you can market and sell to them again in the future. Research shows that 14% of shopping carts that get abandoned are due to being forced to register to the site in order to complete the purchase.(Calkins 2017) A guest checkout avoids this and helps ensure you get the sale. It is also important to ensure that you are set up to take multiple forms of payment. Accepting Visa and Mastercard are great, but a lot of people also use PayPal when shopping online. A shopping cart on your website can help improve your profits and help gain new customers.

</p>
